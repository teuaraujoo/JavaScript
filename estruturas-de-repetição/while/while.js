// While -> Enquanto


// contador básico (1 a 5)

let x = 1; // condição

while(x < 6 /* condição */){
    
    console.log(x);
    
    x++; //contador 
}

// passando pelo array

let array = ['test', 'func', 'damn'];
let y = 0;

while (y <= array.length - 1){

    console.log(array[y]);
    y++;
}

